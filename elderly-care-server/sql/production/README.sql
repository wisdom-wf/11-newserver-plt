-- =============================================
-- 智慧居家养老服务管理平台 - 数据库初始化说明
-- =============================================

-- 一、执行顺序
-- =============================================
-- 执行顺序必须严格按照以下顺序：
-- 1. 01_init.sql        -- 创建数据库、表结构、基础数据
-- 2. 02_area_data.sql   -- 导入区域数据（延安市）
-- 3. 03_menu_permissions.sql  -- 初始化菜单和角色菜单关联
-- 4. 04_permissions.sql -- 初始化权限和角色权限关联
-- =============================================

-- 二、执行命令
-- =============================================
-- 方法1: MySQL命令行
-- mysql -uroot -p --default-character-set=utf8mb4 < 01_init.sql
-- mysql -uroot -p --default-character-set=utf8mb4 < 02_area_data.sql
-- mysql -uroot -p --default-character-set=utf8mb4 < 03_menu_permissions.sql
-- mysql -uroot -p --default-character-set=utf8mb4 < 04_permissions.sql
--
-- 方法2: source命令
-- SOURCE 01_init.sql;
-- SOURCE 02_area_data.sql;
-- SOURCE 03_menu_permissions.sql;
-- SOURCE 04_permissions.sql;
-- =============================================

-- 三、初始化内容说明
-- =============================================
-- 01_init.sql 包含:
--   - CREATE DATABASE elderly_care
--   - 30+ 张表 (用户、角色、权限、服务商、服务人员、老人、订单、预约、日志、质检、评价、财务等)
--   - 超级管理员账户 (admin / admin123)
--   - 基础角色 (R001-R004: SUPER_ADMIN/CITY_ADMIN/DISTRICT_ADMIN/PROVIDER_ADMIN)
--   - 服务类型 (12种助餐/助洁/助浴等)
--   - 数据字典 (字典类型+字典项)
--   - 系统参数
--
-- 02_area_data.sql 包含:
--   - 延安市及13个区县
--   - 街道/社区数据
--
-- 03_menu_permissions.sql 包含:
--   - 菜单表数据 (业务管理9个+系统管理4个菜单)
--   - 角色菜单关联 (PROVIDER_ADMIN可看全部业务菜单, STAFF受限)
-- =============================================

-- 四、默认账户
-- =============================================
-- 超级管理员: admin / admin123
-- 用户类型: SYSTEM
-- 拥有所有权限（在 04_permissions.sql 中分配）
-- =============================================

-- 五、初始化流程说明
-- =============================================
-- 04_permissions.sql 包含:
--   - 权限表数据 (系统管理、服务商、服务人员、老人、订单等模块权限)
--   - 角色权限关联 (超级管理员拥有所有权限)
--   - 管理员角色分配 (admin 用户关联超级管理员角色)
-- =============================================

-- 五、注意事项
-- =============================================
-- 1. 执行前请确保MySQL服务正在运行
-- 2. 执行用户需要有CREATE DATABASE权限
-- 3. 如果数据库已存在，可注释掉01_init.sql中的CREATE DATABASE语句
-- 4. 区域数据使用DELETE + INSERT模式，支持重复执行
-- 5. 菜单权限使用REPLACE INTO模式，支持重复执行
-- =============================================
